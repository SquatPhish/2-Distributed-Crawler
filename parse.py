import ast
import os


def read_files_in_a_dir(folder):
    if not folder.endswith("/"):
        folder = folder + "/"
    files = list()
    for f in os.listdir(folder):
        if f.startswith("phishing") and f.endswith(".json"):
            files.append(folder + f)
    return files


def read_hal_json_to_hashMap(j_file):
    '''
    HAL json analysis
    '''
    url_id_map = dict()
    with open(j_file, 'r') as in_file:
        d = ast.literal_eval(in_file.read())
        embedded = "embedded"
        phishing_urls = d[embedded]["phish"]

        for ele in phishing_urls:
            url = ele['url']
            _id = ele['id']
            brand = ele['brand']
            url_id_map[_id] = url
    return url_id_map


def get_phishing_url_mapping(folder):
    complete_map = dict()
    keys = list()
    for j_file in read_files_in_a_dir(folder):
        print ("Analyzing file {}".format(j_file))
        dic = read_hal_json_to_hashMap(j_file)
        complete_map.update(dic)
        keys.extend(list(dic.keys()))
    print ("LENG", len(keys))
    #separate into 10 chunks
    chunk_size = 10
    each_chunk = len(complete_map)/chunk_size
    keys = complete_map.keys()
    keys.sort()
    print ("UNIQUE LENG", len(list(set(keys))))
    start = 0
    for i in range(chunk_size):
        print ("from the start {}".format(start))
        if i == chunk_size - 1:
            sub_keys = keys[start:]
        else:
            sub_keys = keys[start: start + each_chunk]
        start = start + each_chunk
        sub_dic = {k: complete_map[k] for k in sub_keys}
        outfile = "./chunks/" + str(i) + ".txt"
        save_dic_into_id_url(sub_dic, output=outfile)


def save_dic_into_id_url(dic, output="OUT.txt"):
    keys = dic.keys()
    keys.sort()
    file = open(output,"w")
    for k in keys:
        file.write(str(k) + "," + str(dic[k]) + "\n")
    file.flush()
    file.close()


